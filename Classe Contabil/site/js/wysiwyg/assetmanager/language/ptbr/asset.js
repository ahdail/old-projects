function getTxt(s)
	{
	switch(s)
		{
		case "Cannot delete Asset Base Folder.":return "Não é possível deletar a pasta base";
		case "Delete this file ?":return "Deletar Esse arquivo?";
		case "Uploading...":return "Enviando...";
		case "File already exists. Do you want to replace it?":return "O arquivo já existe, deseja substituí-lo?";
				
		case "Files": return "Arquivos";
		case "del": return "del";
		case "Empty...": return "Vazio...";
		}
	}
function loadTxt()
	{
	var txtLang = document.getElementsByName("txtLang");
	txtLang[0].innerHTML = "Nova&nbsp;Pasta";
	txtLang[1].innerHTML = "Deletar&nbsp;Pasta";
	txtLang[2].innerHTML = "Enviar Arquivo";
	
	var optLang = document.getElementsByName("optLang");
    optLang[0].text = "Todos os Arquivos";
    optLang[1].text = "Media";
    optLang[2].text = "Imagens";
    optLang[3].text = "Flash";
	
    document.getElementById("btnOk").value = " ok ";
    document.getElementById("btnUpload").value = "Enviar";
	}
