<h1 class="titulo">Primeiros passos para conhecer o Neopatrimonialismo</h1>
<div class="divisa"></div>
<p style="line-height: 20px;padding-right: 20px" align="justify">		
<font class="tema">O QUE É O NEOPATRIMONIALISMO </font><br>
Chama-se Neopatrimonialismo uma nova idéia, apoiada em estudos que visam a AJUDAR A PENSAR em Contabilidade. <br>
É um GUIA DO RACIOCINIO que visa a dar ordem ao raciocínio para entender tudo o que se relaciona com a riqueza das empresas e das instituições (que são células sociais). <br>
A preocupação é a VERDADE sobre o que acontece com a riqueza, esta que é a matéria de estudos do contador. <br>
É a primeira vez que na História do Brasil se reúnem milhares de adeptos de uma idéia de cunho científico. <br>

<br>
<font class="tema">COMO NASCEU O NEOPATRIMONIALISMO</font><br>
Tudo nasceu de muitos esforços no sentido de criar uma DOUTRINA CONTABIL BRASILEIRA para mostrar a todo mundo a competência de nossa gente na área contábil. <br>
Italianos, franceses, alemães, portugueses, outros povos já tinham apresentado as suas correntes e essas ganharam vários nomes: Contismo, Personalismo, Aziendalismo, Reditualismo etc. <br>
Como é preciso uma teoria matriz para que se construa uma corrente, partimos da Teoria das Funções Sistemáticas do Patrimônio. <br>
Apresentei essa teoria pela primeira vez na Universidade de Sevilha, na Europa, em 1987. <br>
Depois, na Universidade de Saragoça, ainda na Espanha. <br>
A seguir em Portugal, na Associação Comercial e Industrial de Lisboa. <br>
Daí para frente seguiram difusões em muitos paises e no Brasil editei a primeira obra sobre o assunto em 1992. <br>
Trata-se, pois, de uma doutrina muito MODERNA, recente deveras. <br>

<br>
<font class="tema">COMO CONHECER A IDÉIA NEOPATRIMONIALISTA </font><br>
Existem muitos artigos, dissertações de mestrado, trabalhos universitários, boletins e alguns livros sobre o assunto. <br>
Vários escritores dedicaram-se ao assunto: Prof. Valério Nepomuceno, Prof. Marco Antônio Amaral Pires, Prof. Werno Herckert, Prof. César Kroetz, Professora Ligia Pimenta, Prof. José Antônio Moureira Icó da Silva, Prof. Pedro Onofre Fernandes, Prof. José Jassuipe Morais, Professora Yumara Lúcia Vasconcelos, em suma, diversos intelectuais. <br>
Livros meus que estudam o assunto são os editados pela Atlas de São Paulo: `Teoria da Contabilidade` e `História Geral e das Doutrinas da Contabilidade`. <br>
Pela UNA Editoria existe: `Teoria da Contabilidade Superior`, `Teoria Geral do Conhecimento Contábil` (este também foi editado na Europa pelo Ministério de Economia e Fazenda da Espanha) e `Fundamentos de Contabilidade Geral`. <br>
Em minha página www.lopesdesa.com e em outras de Contabilidade como a de Matosinhos Mateus, Yumara Lúcia de Vasconcelos e outras também se encontra matéria Neopatrimonialista (inclusive em páginas da Europa). <br>
<br>
<font class="tema">BASES DO PENSAMENTO NEOPATRIMONIALISTA </font><br>
Algumas idéias básicas são aceitas para que se formem as concepções do NEOPATRIMONIALISMO e elas são consideradas como VERDADES PRINCIPAIS (Axiomas): <br>
<br>
1. A Contabilidade estuda o patrimônio das empresas e instituições (tais empreendimentos são células sociais); <br>
2. O patrimônio se movimenta; <br>
3. O movimento patrimonial cria variações ou transformações; <br>
4. O patrimônio não se move por si mesmo, necessitando de CAUSAS AGENTES que atuam sobre ele; <br>
5. A finalidade das transformações é, normalmente, suprir necessidades das células sociais; <br>
6. O patrimônio é o meio para satisfação das necessidades e quando ele se movimenta exerce a sua FUNÇÃO; <br>
7. As funções suprem necessidades que se grupam em SISTEMAS; <br>

8. Tais SISTEMAS são os que visam de forma AUTONOMA, mas INTERDEPENDENTE, suprir necessidades de : pagamentos (liquidez), resultados (resultabilidade), equilíbrio (estabilidade), vitalidade (economicidade), eficiência (produtividade), riscos (invulnerabilidade) e dimensão correta (elasticidade); <br>
9. Quando a função supre a necessidade ocorre a EFICÁCIA; <br>
10. Quando a eficácia é permanente e crescente se produz a PROSPERIDADE; <br>
11. Quando todas ou a maioria das células de uma sociedade humana está em estado de prosperidade a sociedade estará também (essa a grande missão da ciência contábil). <br>
