	<h1>O que é?</h1>
	<p>A tecnologia RSS permite aos usuários da internet se inscreverem em sites que fornecem "feeds". São os feeds RSS que recebem estas atualizações, desta maneira o utilizador pode permanecer informado de diversas atualizações em diversos sites sem precisar visitá-los um a um.</p>
	<h1>Como funciona?</h1>
	<p>No arquivo RSS são incluídas informações como título, página (endereço exato de onde há algo novo), descrição da alteração, data, autor, etc. De poucos em poucos minutos o arquivo RSS é atualizado mostrando as alterações recentes. O RSS é um formato padronizado mundialmente para compartilhar conteúdo Web.</p>
	<h1>Como utilizo?</h1>
	<p>Existem duas formas de utilizar o RSS:</p>
    <p>- Através de um programa cliente também denominado de agregador. Nele são incluídos os RSS que o utilizador deseja acompanhar. Ex.: Firefox, Internet Explorer 7</p>
	<p>- Fazendo um registo em sites específicos e neles incluir os RSS que deseja acompanhar. São agregadores via navegador ou browser. Ex.: <a href="http://www.google.com.br/reader/" target="_blank">Google Reader</a>, <a href="http://www.bloglines.com/" target="_blank">Bloglines</a></p>
