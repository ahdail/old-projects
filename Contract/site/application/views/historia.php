	<h4>Origem da Cidade</h4>
	<img class="img-right" src="img/g_Igreja local.jpg"/>
	<p>	
		<br/>
		Uma das primeiras referências ao rio Choró data de 1678, ano em que se deu a concessão ao sargento-mor Estevão Velho de Moura de três léguas de terra na ribeira do choró, que ele tinha descoberto; lá habitavam os índios Jaduins e Paiacus. Paiacus – foi um dos nomes da cidade de pacajus, também variante do nome atual. Sabe-se também terem habitado a mesma região os índios Jaguaribara, integrantes da confederação que motivou a Guerra dos Bárbaros.<br/><br/>
		Cortada pelo rio choró, cujo nome tanto pode significar pequena fonte que surge no sopé ou encosta de uma chapada residual” referência á nascente do rio, na serra do Estevão, Sertão Central do ceará, como” GEMEDOR “, O RONCADOR ”, remissão ao fato de tal curso d’água “ Gemer ” ou “ Roncar ” em épocas de cheia, só se acalmando com a morte de alguém em suas águas , Chorozinho recebe também do rio seu nome , na forma diminutiva.<br/><br/>
		A área de Chorozinho começou a ser povoado com a construção da rodovia federal BR-13, atualmente denominada BR-116, em 1932. <br/><br/>
		Para construir a ponte sobre o rio choró, vieram os primeiros imigrantes; a cidade foi-se formando nas proximidades da Residência, um casarão construído pelo IFOCS (Inspetorias Federal de Obras Contra a Seca ) para alojar os engenheiros responsáveis pela construção da ponte; os “ cassacos ” ( nome recebido pelos operários ) que construíam sua casas nas cercanias.<br/><br/>
		A localidade pertencia ao município de Pacatuba, conforme o decreto nº. 1.156, de 04 de dezembro de 1933, só passando a integrar município de pacajus mediante o decreto nº. 1951, de 23 de novembro 1935, pouco após a emancipação deste município, ocorrida em 23 de maio do mesmo ano.<br/><br/>
		Quando de sua integração a Pacajus, a nova Vila, que se chamaram Currais Velhos e, posteriormente, Currais Novos, recebeu o nome de Chorozinho, pelo Decreto de Lei nº. 448, de dezembro de 1938.<br/>

	
	</p>
		
		<h4>Primeiras Construções</h4>
	<p>           	
		<br/>Com essa construção o DNOCS instalou o acampamento dos engenheiros e operários em um casa a qual denominaram de Residência. <br/><br/>           
		O trabalho da construção deo DNOCS, foi demorada haja vista as dificuldades encontradas na época para transportar o material. Desse modo o DNOCS, contratou muitas pessoas da região para trabalharem na construção da ponte. Assim sendo essas pessoas deslavam-se de suas casas para morarem nas proximidades do seu trabalho. Criando-se assim o povoado.</p>
	
	</p>
		
	<h4>Emancipação Política</h4>
	<p> 
		<br/>Emancipado em 13 de março de 1987, promulgou sua lei orgânica municipal em 05 de abril de 1990, tendo sido atualizada pela revisão constitucional publicada em 17/11/1997,
		atualmente possui uma população de 18.770 (dezoito mil duzentos e setenta) habitantes, distribuídos em 278,400 km 2  de área territorial ,
		distante apenas 60 km de fortaleza capital do estado, integrando um dos nove municípios que compõem a região metropolitana do estado do ceará,
		possuindo um clima tropical quente, e limitando-se com os municípios de Pacajus, Ocara, Cascavel, Acarape e Barreira. </p>
		
	
	</p>
	

	
		<h4>Formação Administrativa</h4>
	<p>			 <br/>Em divisão administrativa referente ao ano de 1933, figura no município de Pacatuba o distrito de Currais Velhos. <br/>           
	Em divisões territoriais datadas de 31-XII-1936 e 31-XII-1937, o distrito de Currais Velhosfigura no município de Guarani. <br/>           
	Pelo decreto estadual nº 448, de 20-12-1938, o distrito de Currais Velhos passou a denominar-se Chorozinho. <br/>
	Pelo decreto-lei estadual nº 1114, de 30-12-1943, o município de Guarani passou a denominar-se Pacajus. <br/>          
	No quadro fixado para vigorar no período de 1939-1943, o distrito já denominado Chorozinho figura no município de Pacajus. <br/><br/>             
	Em divisão territorial datada de 1-VII-1950, o distrito de Chorozinho, figura no municípiode Pacajus. <br/>           
	Assim permanecendo em divisão territorial datada de 1-VII-1960. <br/><br/> 
	
	Elevado à categoria de município com  a denominação de  Chorozinho, pela lei estadual nº 6436, de 17-07-1963, desmembrado de  Pacajus. Sede  no antigo distrito de Chorozinho. Constituído do distrito sede.  <br/>           
	Em divisão territorial datada de 31-XII-1963, o município é constituído do distrito sede. <br/>            
	Pela lei estadual nº 8339, de 14-12-1965, é extinto o município de Chorozinho, sendo seu território anexado ao município de Pacajus, como simples distrito. <br/><br/> 
	Em divisão territorial datada de 31-XII-1968, Chorozinho é distrito de Pacajus. Assim permanecendo em divisão territorial datada de 1988. <br/>           
	Elevado à categoria de município com  a denominação de  Chorozinho, pela lei estadual nº 11305, de 13-03-1987, desmembrado de  Pacajus. Sede no antigo distrito de Chorozinho. <br/>Constituído do distrito sede. Instalado em 01-01-1989. <br/>           Pela lei municipal nº 56, de 25-07-1991, foram criados os distritos de Campestre, Cedro, Patos dos Liberatos, Timbaúba dos Marinheiros e Triângulo. <br/><br/>            
	Em divisão territorial datada de 1991, o município é constituído de 6 distritos: Chorozinho, Campestre, Cedro, Patos dos Liberatos, Timbaúba dos Marinheiros e Triângulo.<br/>            Assim permanecendo em divisão territorial datada de 2007.<br/> 		</p>
